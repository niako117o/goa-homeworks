# рЃЏрЃЮрЃфрЃћрЃЏрЃБрЃџрЃўрЃљ рЃАрЃўрЃљ: names = ["рЃюрЃўрЃЎрЃљ", "рЃЌрЃћрЃЮ", "рЃљрЃюрЃљ", "рЃњрЃўрЃЮ"].
# рЃЊрЃљрЃгрЃћрЃарЃћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф for рЃфрЃўрЃЎрЃџрЃўрЃЌ рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА рЃљрЃЏ рЃАрЃўрЃљрЃерЃў рЃарЃљрЃЏрЃЊрЃћрЃюрЃў рЃАрЃљрЃ«рЃћрЃџрЃў рЃљрЃарЃўрЃА.
names = ["рЃюрЃўрЃЎрЃљ", "рЃЌрЃћрЃЮ", "рЃљрЃюрЃљ", "рЃњрЃўрЃЮ"]
