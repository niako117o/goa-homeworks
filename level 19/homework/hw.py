# рЃЊрЃљрЃгрЃћрЃарЃћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф for рЃфрЃўрЃЎрЃџрЃўрЃЌ 7-рЃ»рЃћрЃа рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА рЃбрЃћрЃЦрЃАрЃбрЃА: РђюрЃЏрЃћ рЃЏрЃўрЃДрЃЋрЃљрЃарЃА рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃарЃћрЃЉрЃљРђЮ.
for i in range(1, 8):
    print('рЃЏрЃћ рЃЏрЃўрЃДрЃЋрЃљрЃарЃА рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃарЃћрЃЉрЃљ')