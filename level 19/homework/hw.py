# 3№ИЈРЃБ рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃЊрЃљрЃЌрЃЋрЃџрЃљ
# рЃЊрЃљрЃгрЃћрЃарЃћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф while рЃфрЃўрЃЎрЃџрЃўрЃЌ рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃА 10-рЃЊрЃљрЃю 1-рЃЏрЃЊрЃћ рЃБрЃЎрЃБрЃдрЃЏрЃљ.
num = 10 
while num >= 1:
    print(num)
    num -= 1  
# 4№ИЈРЃБрЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃ»рЃљрЃЏрЃў
# рЃЊрЃљрЃгрЃћрЃарЃћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф while рЃфрЃўрЃЎрЃџрЃўрЃЌ рЃЊрЃљрЃўрЃЌрЃЋрЃџрЃўрЃА рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃ»рЃљрЃЏрЃА 1-рЃЊрЃљрЃю 50-рЃЏрЃЊрЃћ.
num = 1 
total = 0 

while num <= 50:
    total += num  
    num += 1 

print("рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃ»рЃљрЃЏрЃў 1-рЃЊрЃљрЃю 50-рЃЏрЃЊрЃћ:", total)

# ­Ъћё for рЃфрЃўрЃЎрЃџрЃўрЃА рЃЊрЃљрЃЋрЃљрЃџрЃћрЃЉрЃћрЃЉрЃў

